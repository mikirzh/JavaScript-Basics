"use strict"

/*
4. Реализовать основные 4 арифметические операции (+, -, /, *) в виде функций с двумя параметрами. Т.е. например, функция для сложения должна принимать два числа, складывать их
и возвращать результат.
Обязательно использовать оператор return.
*/

function getSum (a, b) {
	return a + b;
}

function getSub (a, b) {
	return a - b;
}

function getDiv (a, b) {
	return a / b;
}

function getMul (a, b) {
	return a * b;
}